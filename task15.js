var grade='S';

switch(grade){
    case'S':
    console.log("Very good");
    break;

    case'A':
    console.log("Good job");
    break;

    case'B':
    console.log("Good");
    break;

    case'C':
    console.log("Not bad");
    break;

    case'D':
    console.log("Bad");
    break;

    case'E':
    console.log("Fail");
    break;

    default:
        console.log("Unknown grad");

}